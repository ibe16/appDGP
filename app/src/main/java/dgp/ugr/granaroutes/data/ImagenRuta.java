package dgp.ugr.granaroutes.data;

public class ImagenRuta {
    private String name;
    private String url;

    public ImagenRuta() {
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
